    public void populateRestaurantInfo(){
        for(Restaurant restaurant: ecosystem.getRestaurantDirectory().getRestaurantList()){
            if(restaurant.getRestaurantUsername().equals(userAccount.getUsername())){
                txtRestaurantName.setText(restaurant.getRestaurantName());
                txtRestaurantNumber.setText(restaurant.getRestaurantNumber());
                txtRestaurantAddress.setText(restaurant.getRestaurantAddress());
            }
        }
    }
    
