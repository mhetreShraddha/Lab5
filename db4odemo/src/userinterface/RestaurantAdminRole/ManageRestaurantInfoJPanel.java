    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtRestaurantName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Restaurant Name cannot be empty.");
            return;
        }
        if (txtRestaurantAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Restaurant Address cannot be empty.");
            return;
        } else if (txtRestaurantNumber.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Restaurant Number cannot be empty.");
            return;
        }
        String numberFormat = "^\\(?(\\d{3})\\)?[- ]?(\\d{3})[- ]?(\\d{4})$";
        if (Pattern.compile(numberFormat).matcher(txtRestaurantNumber.getText()).matches()) {
            System.out.println("Phone number entered is correct.");
        } else {
            JOptionPane.showMessageDialog(this, "Phone Number entered is not proper, please enter the number in correct format.");
            return;
        }
        
        Restaurant restaurant = ecosystem.getRestaurantDirectory().getRestaurant(userAccount.getUsername());
        ecosystem.getRestaurantDirectory().updateRestaurantInfo(restaurant, txtRestaurantName.getText(), txtRestaurantUsername.getText(), txtRestaurantAddress.getText(), txtRestaurantNumber.getText());

        btnUpdate.setEnabled(true);
        txtRestaurantName.setEnabled(false);
        txtRestaurantNumber.setEnabled(false);
        txtRestaurantAddress.setEnabled(false);
        populateRestaurantInfo();
    }//GEN-LAST:event_btnSaveActionPerformed

