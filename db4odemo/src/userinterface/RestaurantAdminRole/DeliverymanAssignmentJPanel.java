    private void btnOrderAssignmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderAssignmentActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAssignDeliveryman.getSelectedRow();
        if (selectedRow >= 0) {
            DeliveryMan deliveryMan = (DeliveryMan) tblAssignDeliveryman.getValueAt(selectedRow, 0);
            deliveryMan.getOrderList().add(order);
            order.setStatus("Assigned to Deliveryman");
            Customer customer = ecosystem.getCustomerDirectory().getCustomer(order.getCustomerName());
            for (Order ord : customer.getOrderList()) {
                if (ord.getOrderId().equalsIgnoreCase(order.getOrderId())) {
                    ord.setStatus("Assigned to Deliveryman");
                    JOptionPane.showMessageDialog(null, "Status Updated Successfully, Assigned for Deliveryman.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row from the table to assign the order to a Delivery Man");
        }
    }//GEN-LAST:event_btnOrderAssignmentActionPerformed

