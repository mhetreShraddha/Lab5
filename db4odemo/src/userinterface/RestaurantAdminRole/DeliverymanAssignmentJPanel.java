    public void populateDeliveryManTable() {
        DefaultTableModel deliverymanModel = (DefaultTableModel) tblAssignDeliveryman.getModel();
        deliverymanModel.setRowCount(0);
        ArrayList<DeliveryMan> deliveryMan = ecosystem.getDeliveryManDirectory().getDeliverymanList();
        for (DeliveryMan delMan : deliveryMan) {
            Object[] row = new Object[2];
            row[0] = delMan;
            row[1] = delMan.getDeliverymanNumber();
            deliverymanModel.addRow(row);
        }
    }

