    private void btnViewOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewOrderActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrderDetails.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table to view Order Details.");
        } else {
            Order order = (Order) tblOrderDetails.getValueAt(selectedRow, 0);
            ViewOrderDetailsJPanel viewOrderDetailsJPanel = new ViewOrderDetailsJPanel(userProcessContainer, userAccount, ecosystem, order);
            userProcessContainer.add("ViewOrderDetails", viewOrderDetailsJPanel);
            CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
            cardLayout.next(userProcessContainer);
        }

    }//GEN-LAST:event_btnViewOrderActionPerformed

    private void btnAssignDeliveryManActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignDeliveryManActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrderDetails.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table to assign Deliveryman to the Order");
        } else {
            Order order = (Order) tblOrderDetails.getValueAt(selectedRow, 0);
            if(order.getStatus() == "Ready to Deliver") {
                DeliverymanAssignmentJPanel deliverymanAssignmentJPanel = new DeliverymanAssignmentJPanel(userProcessContainer, userAccount, ecosystem, order);
            userProcessContainer.add("DeliveryManAssignment", deliverymanAssignmentJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            }
            else {
                JOptionPane.showMessageDialog(null, "Order cannot be assigned if it is not ready.");
            }
        }
    }//GEN-LAST:event_btnAssignDeliveryManActionPerformed

