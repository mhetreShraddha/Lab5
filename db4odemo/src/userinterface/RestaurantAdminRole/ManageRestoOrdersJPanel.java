    public void populateOrderTable() {
        DefaultTableModel orderDetailsmodel = (DefaultTableModel) tblOrderDetails.getModel();
        orderDetailsmodel.setRowCount(0);
        Restaurant restro = ecosystem.getRestaurantDirectory().getRestaurant(userAccount.getUsername());
        for (Order order : restro.getOrderList()) {
            Object[] row = new Object[5];
            row[0] = order;
            row[1] = order.getCustomerName();
            row[2] = order.getDeliveryAddress();
            row[3] = order.getTotalPrice();
            row[4] = order.getStatus();
            orderDetailsmodel.addRow(row);
        }
    }


